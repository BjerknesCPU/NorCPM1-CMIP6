     &     11.,  11.,  11.,  11.,  12.,  13.,  14.,  14.,  14.,  14., &
     &     14.,  15.,  16.,  16.,  17.,  17.,  18.,  18.,  18.,  24., &
     &     23.,  23.,  24.,  24.,  25.,  29.,  29.,  30.,  31.,  33., &
     &     34.,  36.,  37.,  39.,  43.,  43.,  46.,  47.,  50.,  54., &
     &     54.,  57.,  59.,  69.,  71.,  76.,  77.,  78.,  83.,  91., &
     &     95.,  97., 104., 112., 119., 122., 130., 135., 142., 147., &
     &    156., 173., 184., 174., 188., 191., 194., 196., 210., 236., &
     &    243., 256., 272., 275., 277., 281., 295., 327., 327., 356., &
     &    372., 374., 370., 383., 406., 419., 440., 465., 507., 534., &
     &    552., 566., 617., 624., 663., 707., 784., 750., 785., 819., &
     &    836., 879., 943., 850., 838., 901., 955., 936., 806., 932., &
     &    803., 845., 970., 963., 975., 983.,1062.,1065.,1145.,1053., &
     &    940., 847., 893., 973.,1027.,1130.,1209.,1142.,1192.,1299., &
     &   1334.,1342.,1391.,1383.,1160.,1238.,1392.,1469.,1419.,1630., &
     &   1767.,1795.,1841.,1865.,2043.,2177.,2270.,2330.,2462.,2577., &
     &   2594.,2700.,2847.,3008.,3145.,3305.,3411.,3589.,3801.,4076., &
