     &   6252.,6121.,6129.,6262.,6402.,6560.,6696.,6656.,6522.,6672., &
     &   6842.,6973.,7303.,7910.,8171./

      start_yr=1750
      do i=1,255
        yr_ems(i)=start_yr+i
      enddo
      
! ******************************************************************

      do i=1,255
        if (kplyear.eq.yr_ems(i)) then
          emissions=ems_per_yr(i)
        endif
      enddo

      write(io_stdo_bgc,*) 'CO2_EMS',kplyear,emissions

      RETURN
      END
